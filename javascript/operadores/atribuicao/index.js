/*
Operadores de atribuição
= -> Atribuição
+= -> Adição
-= -> Subtração
/= -> Divisão
*= -> Multiplicação
%= -> Resto
*/

let firstNumber = 10

firstNumber = firstNumber + 2
firstNumber += 2

console.log(firstNumber)

firstNumber -= 2

console.log(firstNumber)

firstNumber /= 2

console.log(firstNumber)

firstNumber *= 2

console.log(firstNumber)

firstNumber %= 2

console.log(firstNumber)